"""
    3 - Faça um algoritmo utilizando o comando while que mostra uma contagem regressiva
    na tela, iniciando em 10 e terminado em 0. Mostrar uma mensagem FIM! após a contagem.
"""

contagem_regressiva = 11

while contagem_regressiva > 0:
    contagem_regressiva = contagem_regressiva - 1
    print(contagem_regressiva)
print('FIM!!!!')